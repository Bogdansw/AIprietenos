function renderFloatingShapes() {
  const colors = ["#ede6ff","#deeeff","#dff5e8","#ffe8dc","#fff5d6","#fde8f0"];
  const wrap = document.getElementById("floating-shapes");
  if (!wrap) return;
  for (let i = 0; i < 12; i++) {
    const size = 40 + Math.random() * 80;
    const el = document.createElement("div");
    el.className = "fshape";
    el.style.cssText = `width:${size}px;height:${size}px;background:${colors[Math.floor(Math.random()*colors.length)]};left:${Math.random()*100}%;top:${Math.random()*100}%;animation-duration:${6+Math.random()*10}s;animation-delay:${-Math.random()*10}s;`;
    wrap.appendChild(el);
  }
}

