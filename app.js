let quizState = { current: 0, score: 0, answered: false };

function startQuizPage() {
  const card = document.getElementById("quiz-card");
  if (!card) return;
  showQuestion();
}

function showQuestion() {
  const card = document.getElementById("quiz-card");
  if (!card) return;
  const q = DATA.quiz[quizState.current];
  const pct = (quizState.current / DATA.quiz.length) * 100;
  card.innerHTML = `
    <div class="quiz-progress">
      <div class="quiz-bar-track"><div class="quiz-bar-fill" style="width:${pct}%"></div></div>
      <span class="quiz-counter">${quizState.current + 1} / ${DATA.quiz.length}</span>
    </div>
    <div class="quiz-question">${q.q}</div>
    <div class="quiz-options">
      ${q.opts.map((opt, i) => `<button class="quiz-opt" onclick="answerQuiz(${i})">${opt}</button>`).join("")}
    </div>
    <div class="quiz-feedback" id="quiz-fb"></div>
    <button class="quiz-next" id="quiz-next" onclick="nextQuestion()">
      ${quizState.current < DATA.quiz.length - 1 ? "Următoarea întrebare →" : "Vezi rezultatul"}
    </button>
  `;
}

function answerQuiz(idx) {
  if (quizState.answered) return;
  quizState.answered = true;
  const q = DATA.quiz[quizState.current];
  const opts = document.querySelectorAll(".quiz-opt");
  const fb = document.getElementById("quiz-fb");
  const nextBtn = document.getElementById("quiz-next");
  opts.forEach(btn => btn.disabled = true);
  opts[q.correct].classList.add("correct");
  if (idx === q.correct) {
    quizState.score++;
    fb.className = "quiz-feedback correct";
    fb.textContent = "Corect! " + q.feedback;
  } else {
    opts[idx].classList.add("wrong");
    fb.className = "quiz-feedback wrong";
    fb.textContent = "Gresit. " + q.feedback;
  }
  nextBtn.classList.add("show");
}

function nextQuestion() {
  quizState.current++;
  quizState.answered = false;
  if (quizState.current >= DATA.quiz.length) {
    showQuizResult();
  } else {
    showQuestion();
  }
}

function showQuizResult() {
  const card = document.getElementById("quiz-card");
  const score = quizState.score;
  const total = DATA.quiz.length;
  const msg = score === total
    ? "Perfect! Ești un expert în AI responsabil!"
    : score >= total / 2
    ? "Bine! Mai ai câte ceva de învățat."
    : "Nu-i nimic! Citește din nou ghidul și încearcă din nou.";
  card.innerHTML = `
    <div class="quiz-result">
      <div class="result-icon">
        <img src="images/icon-trophy.svg" alt="" onerror="this.style.display='none'" />
        <div class="ri-ph"></div>
      </div>
      <h3>${score} din ${total} corecte</h3>
      <p>${msg}</p>
      <button class="quiz-restart" onclick="restartQuiz()">Încearcă din nou</button>
      <a href="index.html" class="quiz-back-link">Înapoi la ghid</a>
    </div>
  `;
}

function restartQuiz() {
  quizState = { current: 0, score: 0, answered: false };
  showQuestion();
}

function initScrollReveal() {
  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add("visible");
    });
  }, { threshold: 0.1 });
  document.querySelectorAll(".reveal").forEach(el => observer.observe(el));
}

